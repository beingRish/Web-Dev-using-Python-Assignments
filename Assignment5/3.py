# 3. Write a python script to swap data of two variables

# input two numbers from users
number_1 = int(input("Enter value:"))
number_2 = int(input("Enter value:"))

# swapping operation
number_1, number_2 = number_2, number_1

# output
print("New value of a number_1 = ", number_1)
print("New value of a number_2 = ", number_2)
