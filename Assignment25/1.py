# 1. Write a python script to create a Profile class with 3 attributes (name, email, age).

# creating Profile class
class Profile:
    # Assigning the values of three Attributes
    name = "Rishabh"
    email = 'rishabhjuly11@gmail.com'
    age = 24


# creating an object
p = Profile()

# printing the values of attributes
print("Name=", p.name)
print("Email=", p.email)
print("Age=", p.age)
